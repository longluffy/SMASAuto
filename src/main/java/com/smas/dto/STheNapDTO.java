package com.smas.dto;

public class STheNapDTO {
	private String mathe;
	private String serial;
	 
	public STheNapDTO(String mathe, String serial) {
		super();
		this.mathe = mathe;
		this.serial = serial;
	}
	public String getMathe() {
		return mathe;
	}
	public void setMathe(String mathe) {
		this.mathe = mathe;
	}
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
	
	
}
